// Write a program that will display all the multiples of 5 from 0 to 35 on one line.
// You are required to use a for loop to accomplish this.
// When completed the output should look like this:
//
// 0 5 10 15 20 25 30 35



public class Prog01
{
	public static void main (String args[])
	{
	for(int k = 0; k<= 35; k +=5)
	    System.out.print(k + " ");
		System.out.println();
		 
	}
}



